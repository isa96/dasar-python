'''
Operator Perbandingan

> Lebih dari
< Kurang dari
>= Lebih dari sama dengan
<= Kurang dari sama dengan
== Sama dengan
!= Tidak sama dengan

Note: Khusus == dan != bisa digunakan untuk membandingkan string

'''
print(1<4)
print(1>4)
print(1>=4)
print(1<=4)
print(1==4)
print(1!=4)
print("a" == "a")
print("b" != "b")